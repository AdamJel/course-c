#include <stdio.h>

int main() {
    printf("The value of   twenty-one is: %d \n", 0x15);  // 21
    printf("The value of   twenty-two is: %d \n", 0x16);  // 22
    printf("The value of    sixty-two is: %d \n", 0x3e);  // 62
    printf("The value of seventy-four is: %d \n", 0x4a);  // 74
    return 0;
}